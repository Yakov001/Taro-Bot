def _sanitize(text: str) -> str:
    """Strip characters Telegram can't render (Private-Use / Unassigned).

    YandexGPT occasionally emits codepoints in the Private Use Area
    (U+E000-U+F8FF etc., category Co) or unassigned codepoints (Cn) that
    appear as tofu / unknown-symbol boxes in Telegram clients.
    """
    return "".join(
        ch for ch in text if unicodedata.category(ch) not in ("Co", "Cn")
    )


async def _call_yandex(system_prompt: str, user_prompt: str) -> str | None:
    try:
        loop = asyncio.get_running_loop()
        response = await loop.run_in_executor(
            None,
            lambda: _get_client().responses.create(
                model=f"gpt://{YANDEX_FOLDER_ID}/{YANDEX_MODEL}",
                input=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_prompt},
                ],
                temperature=0.7,
                max_output_tokens=1000,
            ),
        )
        return _sanitize(response.output[0].content[0].text)
    except Exception:
        logger.error("YandexGPT request failed", exc_info=True)
        return None


def _format_cards(cards: list[dict]) -> str:
    labels = ["Прошлое", "Настоящее", "Будущее"]
    return "\n".join(
        f"{label}: {card['name']} — {card['meaning_short']}"
        for label, card in zip(labels, cards)
    )


async def interpret_spread(question: str, cards: list[dict]) -> str | None:
    user_prompt = f"Вопрос: {question}\n\nКарты:\n{_format_cards(cards)}"
    return await _call_yandex(SYSTEM_PROMPT, user_prompt)


async def interpret_theme(theme: str, cards: list[dict]) -> str | None:
    system = THEME_SYSTEM_PROMPT.format(theme=theme)
    user_prompt = f"Тема: {theme}\n\nКарты:\n{_format_cards(cards)}"
    return await _call_yandex(system, user_prompt)


async def generate_pair_forecast(card_a: dict, card_b: dict) -> str | None:
    """Общий прогноз по двум картам с рандомной позицией каждой."""
    pos_a = "прямая" if random.random() < 0.7 else "перевёрнутая"
    pos_b = "прямая" if random.random() < 0.7 else "перевёрнутая"
    user_prompt = (
        f"Карта первого участника: {card_a['name']} ({pos_a}) — {card_a['meaning_short']}\n"
        f"Карта второго участника: {card_b['name']} ({pos_b}) — {card_b['meaning_short']}\n\n"
        "Составь персонализированный прогноз о динамике в их паре. "
        "Обращайся к ним напрямую: «в вашей паре», «между вами», «ваша связь», «вы оба»."
    )
    return await _call_yandex(PAIR_SYSTEM_PROMPT, user_prompt)